package Osnovi_java;

public class Task16 {
    public static void main(String[] args) {
        for (int i = 10; i < 30; i++) {
            System.out.println(i);
        }

        // Пример с циклом do while
        int start = 10;
        do {
            System.out.println(start);
            start++;
        } while (start < 30);

        // Пример с циклом while

        int startWhile = 10;  // Потому что начинаем с 10
        while (startWhile < 30) {// Сдесь делаем проверку. Пока  startWhile < 30
            System.out.println(startWhile);   // Мы выводим
            startWhile++;    // И увеличиваем
        }


    }
}






















